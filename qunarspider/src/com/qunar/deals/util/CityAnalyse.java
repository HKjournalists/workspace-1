	//过滤重复城市
	public static String filterCity(String[] array) {
		String result = "";

		ArrayList<String> al = new ArrayList<String>();
		for (int i = 0; i < array.length; i++) {
			if (!al.contains(array[i])) {
				al.add(array[i]);
			}
		}
		String[] newAr = new String[al.size()];
		for (int i = 0; i < al.size(); i++) {
			newAr[i] = al.get(i).toString();
			result += newAr[i] + ",";
		}
		if(!result.equals("")){
			result = result.substring(0,result.length()-1);
		}
		return result;
	}
	
